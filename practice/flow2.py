
# primitive comparison

a == b  # equals to each other 
a != b  # does not equal each other
a > b   # is larger than 
a < b   # is smaller than 
a >= b  # is greater or equal than 
a <= b  # is smaller or equal to

# complex comparison 

person is person  # for comparing objects we use is rather than == 
a > b and a < c   # for using two options of comparison(tries the first block first , if true continues to the second)
a > b or a < c    # or using two options of comparison(if either one of the blocks is true then true)